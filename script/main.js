//DISPLAY VIDEO

//WARCRAFT

function warcraftFunction(){
var gameplay = document.getElementById('warcraftVideo');
    if (gameplay.style.display === 'block') {
        gameplay.style.display = 'none';
    } else if (gameplay.style.display === 'none') {
        gameplay.style.display = 'block';
    } else {
        gameplay.style.display = "block";
    }
}
